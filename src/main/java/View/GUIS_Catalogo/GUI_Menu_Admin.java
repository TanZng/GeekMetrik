    public void resizeImage(String foto, JLabel label){
        ImageIcon icon = new ImageIcon( GUISignUp.class.getResource(foto) );
        Image image = icon.getImage();
        Image scale = image.getScaledInstance(label.getWidth(), label.getHeight(), Image.SCALE_SMOOTH);
        ImageIcon scaledIcon = new ImageIcon(scale);
        label.setIcon(scaledIcon);
    }

